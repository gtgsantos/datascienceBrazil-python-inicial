minhaLista = ['Gui', 'Joao']
#é possivel adicionar ou remover novos objetos na lista

minhaTupla = ('Gui', 'Joao')
#tupla nao é mutavel como uma lista, nao se pode remover ou colocar novos objetos na tupla

